module SoluteParMod
!     EQUILIBRIUM CONSTANTS
!
  use data_kind_mod, only : r8 => DAT_KIND_R8
  implicit none
  public
  character(len=*),private, parameter :: mod_filename = &
  __FILE__

  real(r8), parameter :: RNHUI(0:2)=real((/10.0E-02,1.0E-02,0.5E-02/),r8)

!
!     DISSOLUTION AND DISSOCIATION CONSTANTS
!
!     DPH20=water,SPALO=AlOH3s,SPFEO=FeOH3s,SPCAC=CaCO3s,SPCAS=CaSO4s
!     SPALP=AlPO4s,SPFEP=FePO4s,SPCAM=Ca(H2PO4)2s,SPCAD=CAHPO4s
!     SPCAH=hydroxyapatite,SXOX2=R-OH2,SXOX1=R-OH,SXH2P=R-H2PO4
!     SXH1P=R-HPO4,DPCO2=CO2,DPHCO=HCO3,DPN4=NH4,DPAL1=ALOH,DPAL2=ALOH2
!     DPAL3=AlOH3,DPAL4=ALOH4,DPALS=AlSO4,DPFE1=FeOH,DPFE2=FeOH2,DPFE3=FeOH3
!     DPFE4=FeOH4,DPFES=FeSO4,DPCAO=CaOH,DPCAC=CaCO3,DPCAH=CaHCO3
!     DPCAS=CaSO4,DPMGO=MgOH,DPMGC=MgCO3,DPMGH=MgHCO3,DPMGS=MgSO4
!     DPNAC=NaCO3,DPNAS=NaSO4,DPKAS=KSO4,DPH1P=HPO4,DPH2P=H2PO4,DPH3P=H3PO4
!     DPF1P=FeHPO4,DPF2P=FeH2PO4,DPC0P=CaPO4,DPC1P=CaHPO4,DPC2P=CaH4P2O8
!     DPM1P=MgHPO4,DPCOH=R-COO,DPALO=R-AlOH2,DPFEO=R-FeOH2
!
  real(r8), PARAMETER :: DPH2O=6.5E-09_r8     !=[H(+)][OH(-)] in the unit of mol/m3, 
  real(r8), PARAMETER :: SPALO=6.5E-22_r8     !=
  real(r8), PARAMETER :: SPFEO=6.5E-27_r8
  real(r8), PARAMETER :: SPCAC=3.8E-03_r8
  real(r8), PARAMETER :: SPCAS=1.4E+01_r8
  real(r8), PARAMETER :: SPALP=1.0E-15_r8
  real(r8), PARAMETER :: SPFEP=1.0E-20_r8
  real(r8), PARAMETER :: SPCAM=7.0E+07_r8
  real(r8), PARAMETER :: SPCAD=1.0E-01_r8
  real(r8), PARAMETER :: SPCAH=2.3E-31_r8
  real(r8), PARAMETER :: SXOH2=4.5E-05_r8
  real(r8), PARAMETER :: SXOH1=1.1E-06_r8
  real(r8), PARAMETER :: SXH2P=2.0E+07_r8
  real(r8), PARAMETER :: SXH1P=2.0E+07_r8
  real(r8), PARAMETER :: DPCO2=4.2E-04_r8  !CO2 + H2O -> HCO3(-) + H(+), [mol/m3]
  real(r8), PARAMETER :: DPHCO=5.6E-08_r8  !HCO3(-)   -> CO3(2-) + H(+), [mol/m3]
  real(r8), PARAMETER :: DPN4=5.7E-07_r8
  real(r8), PARAMETER :: DPAL1=4.6E-07_r8
  real(r8), PARAMETER :: DPAL2=7.3E-07_r8
  real(r8), PARAMETER :: DPAL3=1.8E-05_r8
  real(r8), PARAMETER :: DPAL4=1.2E-05_r8
  real(r8), PARAMETER :: DPALS=0.16_r8
  real(r8), PARAMETER :: DPFE1=4.6E-07_r8
  real(r8), PARAMETER :: DPFE2=7.3E-07_r8
  real(r8), PARAMETER :: DPFE3=1.8E-05_r8
  real(r8), PARAMETER :: DPFE4=1.2E-05_r8
  real(r8), PARAMETER :: DPFES=7.1E-02_r8
  real(r8), PARAMETER :: DPCAO=12.5_r8
  real(r8), PARAMETER :: DPCAC=4.2E-02_r8
  real(r8), PARAMETER :: DPCAH=13.5_r8
  real(r8), PARAMETER :: DPCAS=1.2_r8
  real(r8), PARAMETER :: DPMGO=0.7_r8
  real(r8), PARAMETER :: DPMGC=0.3_r8
  real(r8), PARAMETER :: DPMGH=67.0_r8
  real(r8), PARAMETER :: DPMGS=2.1_r8
  real(r8), PARAMETER :: DPNAC=0.45_r8
  real(r8), PARAMETER :: DPNAS=3.3E+02_r8
  real(r8), PARAMETER :: DPKAS=5.0E+01_r8
  real(r8), PARAMETER :: DPH1P=4.5E-10_r8
  real(r8), PARAMETER :: DPH2P=6.3E-05_r8
  real(r8), PARAMETER :: DPH3P=7.1_r8
  real(r8), PARAMETER :: DPF1P=4.5E-02_r8
  real(r8), PARAMETER :: DPF2P=3.7E-03_r8
  real(r8), PARAMETER :: DPC0P=3.5E-04_r8
  real(r8), PARAMETER :: DPC1P=1.82_r8
  real(r8), PARAMETER :: DPC2P=40.0_r8
  real(r8), PARAMETER :: DPM1P=1.23_r8
  real(r8), PARAMETER :: DPCOH=1.0E-02_r8
  real(r8), PARAMETER :: DPALO=6.3E+04_r8
  real(r8), PARAMETER :: DPFEO=6.3E+04_r8

  real(r8), PARAMETER :: DPCO3=DPCO2*DPHCO
  real(r8), PARAMETER :: SHALO=SPALO/DPH2O**3
  real(r8), PARAMETER :: SYAL1=SPALO/DPAL1
  real(r8), PARAMETER :: SHAL1=SYAL1/DPH2O**2
  real(r8), PARAMETER :: SYAL2=SYAL1/DPAL2
  real(r8), PARAMETER :: SHAL2=SYAL2/DPH2O
  real(r8), PARAMETER :: SPAL3=SYAL2/DPAL3
  real(r8), PARAMETER :: SYAL4=SPAL3/DPAL4
  real(r8), PARAMETER :: SHAL4=SYAL4*DPH2O
  real(r8), PARAMETER :: SHFEO=SPFEO/DPH2O**3
  real(r8), PARAMETER :: SYFE1=SPFEO/DPFE1
  real(r8), PARAMETER :: SHFE1=SYFE1/DPH2O**2
  real(r8), PARAMETER :: SYFE2=SYFE1/DPFE2
  real(r8), PARAMETER :: SHFE2=SYFE2/DPH2O
  real(r8), PARAMETER :: SPFE3=SYFE2/DPFE3
  real(r8), PARAMETER :: SYFE4=SPFE3/DPFE4
  real(r8), PARAMETER :: SHFE4=SYFE4*DPH2O
  real(r8), PARAMETER :: SHCAC1=SPCAC/DPHCO
  real(r8), PARAMETER :: SYCAC1=SHCAC1*DPH2O
  real(r8), PARAMETER :: SHCAC2=SHCAC1/DPCO2
  real(r8), PARAMETER :: SYCAC2=SHCAC2*DPH2O**2
  real(r8), PARAMETER :: SHA0P1=SPALP/DPH1P
  real(r8), PARAMETER :: SYA0P1=SHA0P1*DPH2O
  real(r8), PARAMETER :: SPA1P1=SYA0P1/DPAL1
  real(r8), PARAMETER :: SYA2P1=SPA1P1/DPAL2
  real(r8), PARAMETER :: SHA2P1=SYA2P1*DPH2O
  real(r8), PARAMETER :: SYA3P1=SYA2P1/DPAL3
  real(r8), PARAMETER :: SHA3P1=SYA3P1*DPH2O**2
  real(r8), PARAMETER :: SYA4P1=SYA3P1/DPAL4
  real(r8), PARAMETER :: SHA4P1=SYA4P1*DPH2O**3
  real(r8), PARAMETER :: SHA0P2=SHA0P1/DPH2P
  real(r8), PARAMETER :: SYA0P2=SHA0P2*DPH2O**2
  real(r8), PARAMETER :: SYA1P2=SYA0P2/DPAL1
  real(r8), PARAMETER :: SHA1P2=SYA1P2/DPH2O
  real(r8), PARAMETER :: SPA2P2=SYA1P2/DPAL2
  real(r8), PARAMETER :: SYA3P2=SPA2P2/DPAL3
  real(r8), PARAMETER :: SHA3P2=SYA3P2*DPH2O
  real(r8), PARAMETER :: SYA4P2=SYA3P2/DPAL4
  real(r8), PARAMETER :: SHA4P2=SYA4P2*DPH2O**2

  real(r8), PARAMETER :: SHF0P1=SPFEP/DPH1P
  real(r8), PARAMETER :: SYF0P1=SHF0P1*DPH2O
  real(r8), PARAMETER :: SPF1P1=SYF0P1/DPFE1
  real(r8), PARAMETER :: SYF2P1=SPF1P1/DPFE2
  real(r8), PARAMETER :: SHF2P1=SYF2P1*DPH2O
  real(r8), PARAMETER :: SYF3P1=SYF2P1/DPFE3
  real(r8), PARAMETER :: SHF3P1=SYF3P1*DPH2O**2
  real(r8), PARAMETER :: SYF4P1=SYF3P1/DPFE4
  real(r8), PARAMETER :: SHF4P1=SYF4P1*DPH2O**3
  real(r8), PARAMETER :: SHF0P2=SHF0P1/DPH2P
  real(r8), PARAMETER :: SYF0P2=SHF0P2*DPH2O**2
  real(r8), PARAMETER :: SYF1P2=SYF0P2/DPFE1
  real(r8), PARAMETER :: SHF1P2=SYF1P2/DPH2O
  real(r8), PARAMETER :: SPF2P2=SYF1P2/DPFE2
  real(r8), PARAMETER :: SYF3P2=SPF2P2/DPFE3
  real(r8), PARAMETER :: SHF3P2=SYF3P2*DPH2O
  real(r8), PARAMETER :: SYF4P2=SYF3P2/DPFE4
  real(r8), PARAMETER :: SHF4P2=SYF4P2*DPH2O**2
  real(r8), PARAMETER :: SHCAD2=SPCAD/DPH2P
  real(r8), PARAMETER :: SYCAD2=SHCAD2*DPH2O
  real(r8), PARAMETER :: SHCAH1=SPCAH/(DPH2O*DPH1P**3)
  real(r8), PARAMETER :: SYCAH1=SHCAH1*DPH2O**4
  real(r8), PARAMETER :: SHCAH2=SHCAH1/DPH2P**3
  real(r8), PARAMETER :: SYCAH2=SHCAH2*DPH2O**7
!
!     MRXN=number of cycles for solving reaction equilibria
!     TPD,TADA,TADC,TSL=adsorpn,solute equilibria
!
  integer, PARAMETER :: MRXN=1
  real(r8), parameter :: TPD=2.5E-03_r8   !1/h,rate constants for dissoln
  real(r8), PARAMETER :: TPDX=TPD/MRXN    !1/h
  real(r8), PARAMETER :: TADA=5.0E-02_r8
  real(r8), PARAMETER :: TADAX=TADA/MRXN
  real(r8), PARAMETER :: TADC=5.0E-02_r8
  real(r8), PARAMETER :: TADCX=TADC/MRXN
  real(r8), PARAMETER :: TADC0=TADC*1.0E-02_r8
  real(r8), PARAMETER :: TSL=5.0E-01_r8
  real(r8), PARAMETER :: TSLX=TSL/MRXN
!
!     DUKM,DUKI=Km,Ki for urea hydrolysis (SOIL SCI 136:56)
!     COOH=CEC of SOC
!     CCAMX=maximum Ca concentration
!     SPNH4,SPNH3,SPNHU,SPNO3,SPPO4=specific rate constants for
!     NH4,NH3,urea,NO3,H2PO4 fertilizer dissolution
!     RNHUI=rate constants for decline in urea hydrolysis inhibition
!
  real(r8), PARAMETER :: DUKM=1.0_r8
  real(r8), PARAMETER :: DUKI=2.5_r8
  real(r8), PARAMETER :: A0=1.0_r8
  real(r8), PARAMETER :: COOH=2.5E-04_r8
  real(r8), PARAMETER :: CCAMX=10.0_r8
  real(r8), PARAMETER :: SPNH4=1.0E-00_r8    ![1/h]
  real(r8), PARAMETER :: SPNH3=1.0E-00_r8    ![1/h]
  real(r8), PARAMETER :: SPNHU=1.0E-01_r8    ![1/h]
  real(r8), PARAMETER :: SPNO3=1.0E-00_r8    ![1/h]
  real(r8), PARAMETER :: SPPO4=5.0E-02_r8    ![1/h]

end module SoluteParMod
